import decimal
# З точністю до 10^-6 обчислити значення функції ln((1+x)/x) за заданою формулою

# Вводимо значення х та ініціалізуємо деякі змінні
x = float(input('Input x: '))
n = 1
a = ((-1)**(n+1)) * (x**(n-1))/ n
n += 1
S = ((-1)**(n+1)) * (x**(n-1))/ n

# Обчислюємо вираз
while ((S-a) > 0.000001) :
    a += ((-1)**(n+1)) * (x**(n-1))/ n
    n += 1
    S += ((-1)**(n+1)) * (x**(n-1))/ n

# Виводимо результат
print('Answer: ',S)